class Operations:
    def __init__(self, no1, no2):
        self.no1 = no1
        self.no2 = no2

    def add(self):
        sum = self.no1 + self.no2
        return sum

    def sub(self):
        subtract = self.no1 - self.no2
        return subtract

    def multi(self):
        multiply = self.no1 * self.no2
        return multiply



